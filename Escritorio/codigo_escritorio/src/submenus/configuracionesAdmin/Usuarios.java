    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        if (tabla.getSelectedRow()>0) {
            if (usuario.get(tabla.getSelectedRow()).getRol()!=2) {
                if (usuario.get(tabla.getSelectedRow()).getRol()==1) {
                    btnAscender.setEnabled(true);
                }
                if (usuario.get(tabla.getSelectedRow()).getRol()==3) {
                    btnDescender.setEnabled(true);
                }
                btnEliminar.setEnabled(true);
            }else{
                btnAscender.setEnabled(false);
                btnDescender.setEnabled(false);
                btnEliminar.setEnabled(false);
            }
        }
    }//GEN-LAST:event_tablaMouseClicked

