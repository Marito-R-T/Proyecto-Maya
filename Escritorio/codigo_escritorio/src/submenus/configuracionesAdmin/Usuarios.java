    private void agregarUsuarios() {
        btnAscender.setEnabled(false);
                btnDescender.setEnabled(false);
                btnEliminar.setEnabled(false);
        usuario = usuarioDb.leerUsuariosArray();
        DefaultTableModel tb = (DefaultTableModel) tabla.getModel();
        int rowCount = tb.getRowCount();
//Remove rows one by one from the end of the table
for (int i = rowCount - 1; i >= 0; i--) {
    tb.removeRow(i);
}
        for (int i = 0; i < usuario.size(); i++) {
            String role;
            switch (usuario.get(i).getRol()) {
                case 2:
                    role="Admin";
                    break;
                case 1:
                    role="Estudiante";
                    break;
                default:
                    role="Lector";
                    break;
            }
            tb.addRow(new Object[]{usuario.get(i).getUsername(), usuario.get(i).getEmail(), usuario.get(i).getNombre(),
                usuario.get(i).getApellido(), usuario.get(i).getNumeroTel(),role});
        }
    }

